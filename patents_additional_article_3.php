            your idea out there. While Google and other Internet search engines are amazingly handy in showing you what’s
            available in the world, don’t discount good old-fashioned books as valuable sources of information. But the
            most important thing you can do at this time, when you’re still working on your idea, is to do a patent search.
            Once you’ve done a patent search, we can move on to trademark searches, which will help you determine whether
            the name you want to brand your product or service with is still available.</p>
        <p class="text-block">
            Most patent documents are available for free online at the Patent Offices of the various countries.
            Importantly, more than 80% of all information contained in patent documents is not contained anywhere else!
            Considering that there are more than 70 million patents searchable free of charge means that patents are one
            of the most valuable resources that any person or company can use when trying to avoid re-inventing the wheel.
            It’s also useful to see how other people have approached the same problem that you’re attempting to solve and
            you will easily be able to see where there are gaps between the various patents that your idea could perhaps
            plug.</p>
