            your concept, then your patent won’t be valid. The problem, of course, is to know whether your concept is
            in fact new or not – in other words, if you have not disclosed the invention, how do you know whether anyone
            else has maybe come up with the same invention before you did? Any such prior documents, prior patents, or
            disclosures are called ‘prior art’ and may be detrimental to the novelty of your invention.
        </p>
