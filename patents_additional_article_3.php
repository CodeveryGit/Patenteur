            Google has, fortunately, gone to great lengths to index US patents and recently they’ve included foreign patents
            as well, so a good place to start is to visit the Google Patents site and type in a few keywords to see what pops
            up. It’s also handy to visit the source of this information – the US Patent and Trademark Office (USPTO) database
            – to see what they’ve got listed.
        </p>
