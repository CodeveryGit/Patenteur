            and you are of the opinion that it is a commercially sensible niche for you to occupy, then it may still be
            worthwhile fighting for a patent to at least secure your foothold in the market.</p>
        <p class="text-block">
            Bear in mind, however, that no patent search can locate all prior art patents. Although we
            have access to more than 70 million patent documents, not all patents are available on the extensive databases
            that we use, simply because not all patents in the world are published. There is also an 18 month delay from
            someone filing a patent to it becoming searchable, so it is simply impossible to give a guarantee that your
            invention is 100% new. The best way to compensate for this is to include as much information as possible in
            your patent application, so that hopefully we have as many fall-back positions available should the patent
            examiner (the patent office employee who examines your patent application) find prior art that we (or you)
