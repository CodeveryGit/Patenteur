        <p class="text-block">
            As mentioned above, most countries have a so-called ‘absolute’ novelty requirement. The US and Australia are
            two of the only exceptions, but even in the US the newly promulgated America Invents Acts means that the
            first person to race to the Patent Office and file their patent application wins the race. Two things are
            of importance here – (i) you must have kept your invention secret until such time as you have protected it
            by way of a provisional patent application; and (ii) the same invention must not have been patented,
            disclosed, manufacture, sold or exhibited by anyone else. </p>
        <p class="text-block">At the outset it’s important to realize that it’s impossible to determine whether your
            invention is 100% novel, i.e. new. However, it is useful to first conduct a patent search, so that you at
            least have an idea of how much attention your field of invention has received from other inventors. If you
            find something that knocks out your invention, well then at least you can stop lying awake about it at night
            and move on to the next new idea. If it isn’t knocked out, then you’re ready for the next step.</p>
        <p class="text-block">While there are several patent databases that provide substantial global coverage all in
            one place, these databases are usually quite costly. I would always recommend having the best search done by
            patent professionals, provided you can afford this. However, as your idea is still untested, most inventors
            prefer to conduct a cursory search through some of the free websites, before filing a provisional application.
            Should you find out at a later stage that your invention is indeed not novel, then you can always elect to
            have your patent lapse or to abandon it without incurring any further costs. This, unfortunately, is one of
            the realities of the patenting process. </p>
        <p class="text-block">When conceptualizing your idea, try the following free sites that are easily searchable
            and provide details of over 30 million patents and patent applications when combined: </p>
        <p class="text-block">(i) Google Patents. Google has gone to great lengths to ensure that it captures in
            electronically searchable form every single US patent filed between 1790 and the present day. It’s an easily
            searchable and user-friendly interface which makes it very easy to at least get an idea of how densely
            populated your field of invention is. Google has to date only included US patents, but they are working
            steadily toward including patents from the European Patent Office (see below) and other Patent Offices. Go
            into Google, type in Google Patents, and you’ll be taken to an awesome collection of some of the most easily
            searchable patent documents available free of charge. One of the main advantages of this site is that you get
            to use the Google algorithms to scan patent documents, which means that you get the most relevant results
            first. It’s also the best way of seeing the patent drawings at a glance, which is often helpful when you
            have many documents to work through. </p>
        <p class="text-block">(ii) The next is the US Patent and Trademark Office website, available at: http://www.uspto.gov.
            This website only covers patents filed at the United States Patent Office (by American and foreign companies
            and individuals) but does not include patents filed in other countries. (iii) In addition, we would recommend
            searching the European Patent Office website, available on-line at: ep.espacenet.com. This website contains
            patents and patent applications not only from Europe, but also from several other countries. It’s not as
            easily searchable as the USPTO website, and may take a little more time to extract the best results from it. </p>
