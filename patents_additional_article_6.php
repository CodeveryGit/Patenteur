        <p class="text-header-block">What is Patent “Inventiveness”?</p>
        <p class="text-block">Even if your invention is new, there may be other similar inventions that have been
            disclosed to the public in patent specifications or other literature. All such prior disclosures are known
            as “prior art”. Now, the test for inventiveness under the patent laws of most countries is to conjecture
            whether someone skilled in the field of the invention (called a person “skilled in the art” or person having
            ordinary skill in the art – PHOSITA) would consider your invention to be obvious or not. This means that an
            invention that may be considered obvious to a person skilled in the art is not inventive. Because this test
            is highly subjective and has many shades of grey, the decision to file a patent application is often dictated
            more by whether it’s novel or not, and whether it forms part of a sensible business strategy.</p>
