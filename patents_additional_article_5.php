        <p class="text-block">
            To start off with, you can do two types of novelty searches to see what has been done before (these are
            called ‘novelty searches’ as they give you an indication of whether your idea is new or not). The first type
            of search is one in which you search for patent using the names of companies or individuals that you know
            have been active in your field of interest.</p>
        <p class="text-block">
            A second type of search is a keyword search, much like you would use keywords to search for information on
            the World Wide Web. Either way, you will be astounded by the amount of information you will discover. Keep
            on refining your searches using synonyms and some of the words that you pick up in the patent documents
            (called ‘patent specifications’) that you find. As mentioned above, you may also want to search through the
            patent databases of the European, Canadian, and Australian Patent Offices (just type in the name of the
            Patent Office into a search engine and you’ll most likely find it quite easily).
        </p>
        <p class="text-block">
            The easiest way to get the patent information you need, though, is to go to Google, type
            in Google Patents and, once you hit the Google Patents page, type in your keywords to see what pops up. This
            will give you a good idea of the patent landscape that you will be playing in, but bear in mind that many
            patents are published in languages other than English, so you might miss them. Google can give you machine
            translation of certain foreign texts, but not all of them. Because patents are indexed using not only
            keywords but also huge amounts of bibliographic information, you’ll be able to find the names of other
            companies active in your field of expertise, see where they are located, see who cited their patents (another
            useful lead to find technology in your field), and see who individual inventors in your field are. </p>
        <p class="text-block">
            Some companies use these records to identify prolific inventors in their field of expertise that they can
            then try and poach from their competitors. While you’re doing this, make sure you write down all your
            thoughts regarding your idea – the brand, the look & feel, the advantages, the disadvantages, the various
            components, variations of your idea that could work and the like. Also, keep notes on how you came up with
            the various ideas, and how your product or service is different from others. You will also see that it is
            possible to search using the patent classes into that patents are classified. These classes are only useful
            if you have an exact description of your product and you wish to limit your search to a particular
            application only. </p>
        <p class="text-block">
            Remember that no search can ever be considered exhaustive or conclusive, as not all the patent records of
            all the countries are searchable on-line. In addition, not all novelty-destroying disclosures may have been
            made in patent specifications – such disclosures may also have been made in trade journals or scientific
            literature related to your field. In addition, the patent records for your country might not even be
            available online. It’s always worth travelling down to your local Patent Office or getting a patent attorney
            to do a search through your country’s records to ensure that you are free to make and use your idea (whether
            you have a patent for it or not) – this is explained in the section dealing with patent infringement searches.</p>
        <p class="text-block">
            The patent searches should be done as thoroughly as possible, but you must always remember that your results
            are dependent on your choice of keywords and the accuracy and completeness of the patent records you are
            searching. Bear in mind also that there may be a potentially damaging disclosure out there that you could
            miss completely – this is one of the reasons why you should put as much information into a patent
            specification at the time of writing it (discussed below), so that you have as many potential fallback
            positions as possible, should earlier documents that pre-date your patent filing date (also called the ‘prior
            art’) come to the fore at a later stage.
        </p>
