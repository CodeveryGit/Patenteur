        <p class="text-header-block">Who can help me create a prototype of my invention?</p>
        <p class="text-block">When you’re looking for a prototyper, you should go about it in much the same way as
            looking for a manufacturer: network with similar professionals, look on the Internet or in relevant
            publications, or search under headings like ‘designers, industrial’ and ‘engineer, product development’. </p>
        <p class="text-block">Before you contact a prototyper, you should have a clear picture of exactly what you
            need. Never approach a prototyper with rough sketches or a vague verbal description. Get a draftsperson
            to draft technical drawings of your invention. They don’t have to be intricately detailed, but they
            should include all essential dimensions and details required for the prototype to be constructed.
            Remember – it’s far less costly to change a design mistake on paper than it is to try to modify something
            that’s already in metal or plastic. </p>
        <p class="text-block">As mentioned before: If you hire a drafting, design or engineering firm to help create
            your product, you can very well end up with a co-inventor. If that engineer is required to do any real
            engineering and they come up with patentable matter, they must be cited as inventors in the patent application.
            To prevent that person from owning the invention along with you, you need an invention assignment agreement in
            conjunction with the actual consulting contract. The agreement specifies that you are commissioning the person
            to assist you, but that any patentable matter that comes out of this relationship will be assigned to you.</p>
        <p class="text-block">You still need to name the engineer or prototyper on your patent as an inventor, but if you
            have the agreement completed before you file, then that person has no rights to your patent. It may seem like
            a minor formality, but if you neglect to do it, the engineer could have equal rights to your invention.</p>
        <p class="text-block">Once you have created a prototype, you should conduct prototype testing. Make sure the
            product does what people want and performs well enough for people to buy it.Also, be sure your product has
            all the features end-users want. Let them try out the product, and gauge their responses. If at all possible,
            you should consider making a prototype for both mechanical testing and for market research. Do this no matter
            how sure you are that the product will sell. </p>
        <p class="text-block">Most inventions must go through three or four prototypes before they are perfected from both
            a manufacturing and a marketing point of view. In the long run, making those prototypes saves you lots of money
            and allows you to produce the best possible product.   Filed Under: intellectual property, IP, patent, patent
            advice, patenting, prototype </p>
