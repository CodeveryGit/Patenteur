        <p class="text-block">
            As a patent law practice, we can’t provide commercial advice but we can certainly tell what we’ve seen has
            worked and what hasn’t worked based on experience gained from working with other clients and the patenting
            process. Basically, after you’ve filed your provisional patent application, done your market research and
            determined that a market exists for your invention, you should consider creating a prototype and conducting
            testing. </p>
        <p class="text-block">
            A prototype is a model of your invention that puts all of the things you have written in your provisional
            patent application into practice and can be used to woo funders, manufacturers, or distributors.</p>
        <p class="text-header-block">Why should you create a prototype?</p>
        <p class="text-block">
            Although there is no prerequisite that patentees have to build a prototype of their invention, the most
            obvious reason for creating a prototype is to have something tangible to present to potential investors and
            buyers. </p>
        <p class="text-block">
            Prototypes demonstrate the functionality of your invention and help you to discover more patentable details
            of your invention before you file your complete patent application. In general, at this early stage try to
            prototype as cheaply as possible. Stephen Key’s excellent ‘One Simple Idea’ contains a fantastic section on
            cheap and rapid prototyping. </p>
        <p class="text-block">
            In general, most interested parties that you approach won’t need a prototype that they can touch or interact
            with. Having a physical prototype is obviously first prize, but it’s amazing how far you can go with 3D
            renderings, videos, and photos.</p>
        <p class="text-header-block">How to prototype cheaply and easily</p>
        <p class="text-block">These days, you can use simple (and free!) programs such as Google’s ‘Sketch-up’ to very
            easily and quickly render a 3D model of your idea that you can show to interested parties in a presentation.
            This program is very easy to use and you could get a decent prototype drawing done within a morning. </p>
        <p class="text-block">You can also build a cardboard mockup of your idea and take photos of it. From most angles
            most people won’t even be able to tell that it’s made of cardboard if you play around with materials and
            lighting conditions. </p>
        <p class="text-block">Better yet, create a video of it and load it to an online player with a confidentiality
            setting. YouTube, for example, has a setting which keeps the video private and which allows you to control
            access to it by way of links and/or passwords. This can save you an enormous amount of money and still get
            the same results (or nearly the same results) than you would have if you had engaged the services of a
            prototype or industrial design company. </p>
        <p class="text-block">As mentioned before, it is important that you ensure that you own the rights to the design,
            so that you are in fact the IP owner. Have a look at the IP assignment agreement that I’ve included in your
            workbook – you’ll see that it specifically says that the rights associated with any design are assigned to
            you or your company, whichever you prefer. Keep in mind that a complete patent application protects the
            ‘perfected’ form of the invention.</p>
        <p class="text-block">As you proceed along the road of developing your invention, you will almost always
            discover a flaw in your original design or think of a new feature you would like to add. If you obtain
            final/complete patent protection for your idea before you work out these snags, it will be too late to
            include them in the patent and you will risk losing the patent rights of the new or improved design to
            someone else. </p>
        <p class="text-block">You should try to get a prototype made in a way that creates a version of your product
            that matches your final product as closely as possible, but in a small quantity. There are two major
            advantages to having multiple ‘product quality’ prototypes: You can have multiple people looking at them
            and not worry much if one gets broken or is not returned, and you can put some on the market for sale for
            real and see what actually happens when buyers have the opportunity to purchase them. </p>
        <p class="text-block">Obtaining actual proof that the product will sell before you spend the large amounts of
            money often required for production tooling will substantially reduce the risk of you losing that money. Of
            course, make sure that the version that you’re putting on the market is protected with at least a provisional
            patent application and/or a design registration application (whichever is most appropriate, or both).</p>
